#Arrays are used to store multiple values in one single variable.
cars=["Ford","Volv","BMW"]
print(cars)
# Access the Elements of an Array 
x=cars[0]
print(x)#Ford
#Modify the value of the first array item
cars[0]="Toyota"
print(cars)#['Toyota', 'Volv', 'BMW']
#The Length of an Array
x=len(cars)
print(x)#3
